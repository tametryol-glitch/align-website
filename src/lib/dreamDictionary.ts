export function getSymbol(key: string): DreamSymbolEntry | undefined {
  return DREAM_SYMBOLS.find(s => s.key === key);
}

export function searchSymbols(query: string): DreamSymbolEntry[] {
  if (!query.trim()) return DREAM_SYMBOLS;
  const q = query.toLowerCase();
  return DREAM_SYMBOLS.filter(s =>
    s.name.toLowerCase().includes(q) ||
    s.general.toLowerCase().includes(q) ||
    s.key.toLowerCase().includes(q)
  );
}
